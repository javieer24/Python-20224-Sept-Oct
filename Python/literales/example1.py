### Ejercicio 2: Uso de Notación Científica
###Escribe un programa que utilice literales en notación científica para calcular el producto de dos números grandes.

###**Ejemplo:**
numero_grande_1 = 3e8  # 3 x 10^8
numero_grande_2 = 2e6  # 2 x 10^6

resultado = numero_grande_1 * numero_grande_2
print("El producto es:", resultado)

### Ejercicio 3: Cadenas de Texto con Comillas
###Crea un programa que defina tres variables con cadenas de texto diferentes. En una de ellas, incluye comillas dentro de la cadena utilizando el carácter de escape (`\`) y en otra, utiliza apóstrofes para delimitar la cadena.
